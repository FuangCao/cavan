int cavan_font_init(struct cavan_font *font)
{
	byte *body;
	const byte *p, *p_end;

	font->width = 960;
	font->height = 18;

	body = malloc(font->width * font->height);
	if (body == NULL)
	{
		pr_error_info("malloc");
		return -ENOMEM;
	}

	font->body = body;
	font->cwidth = 10;
	font->cheight = 18;
	font->stride = font->width * font->cheight;

	for (p = font_10x18_data, p_end = p + NELEM(font_10x18_data); p < p_end; p++)
	{
		int count = (*p) & 0x7F;

		mem_set(body, ((*p) & (1 << 7)) ? 0xFF : 0, count);
		body += count;
	}

	return 0;
}

void cavan_font_deinit(struct cavan_font *font)
{
	if (font->body)
	{
		free(font->body);
	}
}
